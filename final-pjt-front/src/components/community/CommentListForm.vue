<template>
  <form @submit.prevent="onSubmit" class="comment-list-form mt-5 pt-5">
    <div id="set">
      <hr>
      <input type="text" id="comment-create" v-model="content" class="form-control" placeholder="댓글을 입력하세요.." required>
      <button id="comment-btn">댓글 등록</button>
    </div>
  </form>
</template>

<script>
import { mapGetters, mapActions } from 'vuex'

export default {
  name: 'CommentListForm',
  data() {
    return {
      content: ''
    }
  },
  computed: {
    ...mapGetters(['article']),
  },
  methods: {
    ...mapActions(['createComment']),
    onSubmit() {
      this.createComment({ articlePk: this.article.pk, content: this.content, })
      this.content = ''
    }
  }
}
</script>

